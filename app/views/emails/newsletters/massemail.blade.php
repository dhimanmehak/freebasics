<p class="bodytext"><b><span lang="EN-GB"><i>Every child has the right to go to school, but millions are still being left behind.</i> </span></b></p>

<p class="bodytext"><span lang="EN-GB">Universal primary education involves entering school at an appropriate age, progressing through the system and completing a full cycle. </span></p>

<p class="bodytext"><span lang="EN-GB">Today,&nbsp; </span>there are over 30 million more children in school than in the beginning of the decade. There have been some remarkable success stories. Primary school enrolments have increased dramatically in sub-Saharan Africa as well as in in South and West Asia. In Ethiopia there are three million more children in school than in 2000, thanks to an ambitious rural school construction programme and the abolition of primary school fees - a widespread obstacle to universal primary education.<br />
However, there are 72 million children still out of school. Nearly half of these children live in sub-Saharan Africa. On current trends, 56 million children could still be out of school by 2015.</p>

<p class="bodytext"><span lang="EN-GB">Of those students enrolled in school, millions drop out or leave school without having gained the most basic literacy and numeracy skills. Additionally, pupil/teacher ratios in many countries are in excess of 40:1 and a severe teacher shortage exists.</span></p>

<p class="bodytext"><span lang="EN-GB">Many governments are neglecting the &ldquo;education poor&rdquo; &ndash; those on the fringes of society, ranging from indigenous populations to street children, from the disabled to linguistic and cultural minorities. New approaches must be tailor-made for such groups &ndash; simply increasing opportunities for standard schooling is not enough.</span></p>

<p class="bodytext"><span lang="EN-GB">Unless we reach the children who are being left behind, the goal of education for all children will not be reached. </span></p>
